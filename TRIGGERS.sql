   CENA_ZA_DOBE = CENA_ZA_DOBE+10
WHERE
   STATUS_SPRZETU ='DOBRY';
   
SELECT *FROM SPRZET;
SELECT *FROM MAGAZYN;


DROP TRIGGER wypozyczenie_magazyn_trg;
